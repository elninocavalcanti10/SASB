    public void mostrar () {
        ActionListener acao = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (jProgressBar1.getValue() >= 100) {
                    JfrmLogin.main(null);
                    dispose();
                    t.stop();
                } else {
                    jProgressBar1.setValue(jProgressBar1.getValue() + 2);
                }
            }
        };
        
        t = new Timer(100, acao);
        t.start();
    }

